function modeBtn() {
    document.querySelector('body').classList.toggle('darkMode')
}


// O .classList.add() -> Adiciona uma classe no elemento selecionado
// O .classList.remove() -> Remove uma classe no elemento selecionado
// O .classList.toggle() -> Se o elemento não tiver a classe instanciada, ele adiciona, se tiver a classe ele remove.
// O .classList.contains() -> Pergunta se o elemento tem a classe ou não. Retorna um valor verdadeiro ou falso.