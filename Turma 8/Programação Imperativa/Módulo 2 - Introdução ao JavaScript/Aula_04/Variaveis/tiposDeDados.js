// Numéricos (number)

let num = 35
console.log(num)

let preco = 35.5
console.log(preco)

// Cadeia de caracteres (string)

let nome = 'Douglas Silva'
console.log(nome)

let cargo = 'Estagiário'
console.log(cargo)

// Lógicos ou booleanos (boolean)

let cursoLegal = true
console.log(cursoLegal)

let falteiNaAula = false
console.log(falteiNaAula)

// NaN (Not a Number) -> Indica que o valor não pode ser passado como um número

let divisaoImperfeita = '35a' / 2
console.log(divisaoImperfeita)

// Null (Valor Nulo) -> Indica que o valor está vazio ou desconhecido

let temperatura = null
console.log(temperatura)

// Undefined (sem valor definido) -> Indica a ausência de valor. As variáveis tem um valor indefinido, até que seja atribuído algum valor.

let saudacao //não tem valor
console.log(saudacao)

saudacao = 'Olá Mundo !'
console.log(saudacao)

// Objeto Literal

let carro = {
  marca: 'Fiat',
  ano: '2019',
  cor: 'cinza'
}
console.log(carro)

// Array

let series = ['Friends', 'The100', 'A Origem de Tudo']
console.log(series)
